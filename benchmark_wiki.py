def _ascii_sparkline(values):
    """Render a list of numbers as an ASCII sparkline."""
    if not values:
        return ""
    mn, mx = min(values), max(values)
    span = mx - mn if mx != mn else 1
    blocks = " ▁▂▃▄▅▆▇█"
    return "".join(blocks[min(len(blocks) - 1, int((v - mn) / span * (len(blocks) - 1)))]
                   for v in values)


def _ascii_bar(value, max_val=100, width=30):
    """Render a single value as a horizontal bar."""
    filled = int((value / max_val) * width)
    return "█" * filled + "░" * (width - filled)


