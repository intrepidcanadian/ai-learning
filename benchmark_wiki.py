def show_diff():
    """Compare the two most recent benchmarks."""
    BENCHMARK_DIR.mkdir(parents=True, exist_ok=True)
    files = sorted(BENCHMARK_DIR.glob("*.json"))
    if len(files) < 2:
        print("Need at least 2 benchmark runs to diff. Run the benchmark again.")
        return

    with open(files[-2]) as f:
        old = json.load(f)
    with open(files[-1]) as f:
        new = json.load(f)

    old_ver = old.get("version", 1)
    new_ver = new.get("version", 1)

    print(f"  DIFF: {files[-2].stem}  ->  {files[-1].stem}")
    if old_ver != new_ver:
        print(f"  (v{old_ver} -> v{new_ver} -- article count may differ)")
    print("=" * 70)

    old_comp = old["summary"]["overall_composite"]
    new_comp = new["summary"]["overall_composite"]
    delta = new_comp - old_comp
    arrow = "+" if delta > 0 else "" if delta < 0 else "="
    print(f"  Overall: {old_comp} -> {new_comp} ({arrow}{delta:.1f})")
    old_count = old["summary"]["article_count"]
    new_count = new["summary"]["article_count"]
    if old_count != new_count:
        print(f"  Articles: {old_count} -> {new_count}")
    print()

    header = f"  {'Article':<32} {'Before':>7} {'After':>7} {'Delta':>7} {'Note':>6}"
    print(header)
    print("  " + "-" * 55)

    all_keys = sorted(set(list(old["articles"].keys()) + list(new["articles"].keys())))
    for key in all_keys:
        old_score = old["articles"].get(key, {}).get("scores", {}).get("composite", None)
        new_score = new["articles"].get(key, {}).get("scores", {}).get("composite", None)

        if old_score is None and new_score is not None:
            print(f"  {key:<32} {'---':>7} {new_score:>7.1f}         NEW")
        elif old_score is not None and new_score is None:
            print(f"  {key:<32} {old_score:>7.1f} {'---':>7}      REMOVED")
        else:
            d = new_score - old_score
            arrow = "+" if d > 0 else "" if d < 0 else " "
            print(f"  {key:<32} {old_score:>6.1f} {new_score:>7.1f} {arrow}{d:>5.1f}")

    print()


