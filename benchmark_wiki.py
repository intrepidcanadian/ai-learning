EXPERIMENT_DIR = ROOT / "data" / "experiments"


def _load_experiment_notes():
    """Load experiment logs and build a mapping from after_score -> short note."""
    notes = {}
    exp_files = sorted(EXPERIMENT_DIR.glob("*.json"))
    for f in exp_files:
        try:
            with open(f) as fp:
                exp = json.load(fp)
        except (json.JSONDecodeError, OSError):
            continue

        # Extract after_score from various experiment formats
        after = None
        strategy = ""
        result = exp.get("result", {})
        if isinstance(result, dict):
            after = (result.get("score_after") or result.get("after_composite")
                     or result.get("after"))
            strategy = exp.get("hypothesis", {}).get("strategy", "")
        if after is None:
            after = exp.get("after_score") or exp.get("after")
        # Handle nested dict format (e.g. {"composite": 80.6, ...})
        if isinstance(after, dict):
            after = after.get("composite")
        if not strategy:
            strategy = exp.get("strategy", "")

        if after is not None and isinstance(after, (int, float)) and strategy:
            short = strategy[:80] + ("..." if len(strategy) > 80 else "")
            notes[after] = short
    return notes


def generate_trend_md():
    """Generate research-sources/benchmark-trend.md from all benchmark history."""
    BENCHMARK_DIR.mkdir(parents=True, exist_ok=True)
    files = sorted(BENCHMARK_DIR.glob("*.json"))

    lines = [
        "# Quality Benchmark",
        "",
        "Automated quality scores for the wiki's articles across all categories. "
        "Articles are scored with tier-aware expectations: **flagship** articles face "
        "the highest quality bar, **standard** articles have moderate expectations, "
        "and **reference** articles are evaluated primarily on linking and consistency.",
        "",
        "Scores are computed by [`benchmark_wiki.py`](../benchmark_wiki.py) across seven dimensions: "
        "structural completeness, depth, currency, sourcing, internal linking, consistency, and freshness. "
        "See [Wiki Quality Benchmarking](../methodologies/wiki-quality-benchmarking.md) for full methodology.",
        "",
    ]

    if not files:
        lines.append("*No benchmark data yet. Run `python3 benchmark_wiki.py` to generate.*")
        lines.append("")
        TREND_MD.write_text("\n".join(lines))
        return

    # Load latest run
    with open(files[-1]) as fp:
        latest = json.load(fp)

    latest_summary = latest["summary"]
    latest_ver = latest.get("version", 1)

    # ── Current Score ──
    lines.append("## Current Score")
    lines.append("")
    lines.append(f"**Overall: {latest_summary['overall_composite']}/100** | "
                 f"{latest_summary['article_count']} articles | "
                 f"{latest_summary['total_words']:,} words")
    lines.append("")

    tier_composites = latest_summary.get("tier_composites", {})
    if tier_composites:
        tier_parts = [f"**{t.title()}:** {s}" for t, s in tier_composites.items()]
        lines.append(f"Tier scores: {' | '.join(tier_parts)}")
        lines.append("")

    coverage = latest.get("coverage", {})
    if coverage:
        lines.append(f"Coverage: {coverage.get('substantial_count', '?')} substantial | "
                     f"{coverage.get('stub_count', '?')} stubs ({coverage.get('stub_pct', '?')}%)")
        lines.append("")

    freshness = latest.get("freshness", {})
    if freshness:
        lines.append(f"Freshness: {freshness.get('freshness_pct', '?')}% updated this week | "
                     f"median age: {freshness.get('median_age_days', '?')} days")
        lines.append("")

    topic_cov = latest.get("topic_coverage", {})
    wishlist_cov = latest.get("wishlist_coverage", {})

    if topic_cov or wishlist_cov:
        lines.append("### Coverage Metrics")
        lines.append("")
        lines.append("| Metric | Score | Detail |")
        lines.append("|--------|-------|--------|")

    if topic_cov:
        cov_score = topic_cov.get("coverage_score", "?")
        resolved = topic_cov.get("resolved", "?")
        total = topic_cov.get("total_topic_refs", "?")
        unresolved = topic_cov.get("unresolved", 0)
        lines.append(f"| **Topic Coverage** | {cov_score}/100 | "
                     f"{resolved}/{total} cross-link references resolved, {unresolved} gaps |")

    if wishlist_cov and wishlist_cov.get("total", 0) > 0:
        wl_score = wishlist_cov.get("coverage_score", "?")
        wl_written = wishlist_cov.get("written", "?")
        wl_total = wishlist_cov.get("total", "?")
        wl_remaining_count = wl_total - wl_written if isinstance(wl_total, int) and isinstance(wl_written, int) else "?"
        lines.append(f"| **Wishlist Coverage** | {wl_score}% | "
                     f"{wl_written}/{wl_total} planned topics written, {wl_remaining_count} remaining |")

        # Combined coverage
        topic_score = topic_cov.get("coverage_score", 100) if topic_cov else 100
        if isinstance(topic_score, (int, float)) and isinstance(wl_score, (int, float)):
            combined = (topic_score + wl_score) / 2
            mode = "Quality" if combined >= 80 else "Coverage"
            lines.append(f"| **Combined Coverage** | {combined:.0f}% | "
                         f"Average of topic + wishlist — mode: **{mode}** |")

    lines.append("")

    if topic_cov:
        gaps = topic_cov.get("gaps", [])
        if gaps:
            lines.append("**Red-link gaps** (topics referenced but no article exists):")
            lines.append("")
            lines.append("| Topic | References | Suggested Category |")
            lines.append("|-------|-----------|-------------------|")
            for gap in gaps[:15]:
                refs = ", ".join(gap["referenced_by"][:3])
                if len(gap["referenced_by"]) > 3:
                    refs += f" +{len(gap['referenced_by']) - 3}"
                lines.append(f"| {gap['topic']} | {refs} | {gap['suggested_category']} |")
            lines.append("")

    if wishlist_cov and wishlist_cov.get("total", 0) > 0:
        remaining_items = [i["topic"] for i in wishlist_cov.get("items", []) if not i.get("written")]
        if remaining_items:
            lines.append("**Remaining wishlist topics:**")
            lines.append("")
            for item in remaining_items:
                lines.append(f"- {item}")
            lines.append("")

    lines.append(f"*Last benchmarked: {latest['timestamp'][:16]}*")
    lines.append("")

    # Per-category tables
    if latest_ver >= 2:
        by_cat = defaultdict(list)
        for a in latest["articles"].values():
            by_cat[a.get("category", "unknown")].append(a)

        for cat in CATEGORIES:
            cat_articles = by_cat.get(cat, [])
            if not cat_articles:
                continue
            cat_label = CATEGORY_LABELS.get(cat, cat)
            lines.append(f"### {cat_label}")
            lines.append("")
            lines.append("| Article | Tier | Struct | Depth | Currency | Sourcing | Linking | Consist | Fresh | **Score** |")
            lines.append("|---------|------|--------|-------|----------|----------|---------|---------|-------|-----------|")
            for a in sorted(cat_articles, key=lambda x: -x["scores"]["composite"]):
                s = a["scores"]
                lines.append(
                    f"| {a['key']} | {a['tier']} | {s['structural']} | {s['depth']} | "
                    f"{s['currency']} | {s['sourcing']} | {s['linking']} | {s['consistency']} | "
                    f"{s['freshness']} | **{s['composite']}** |")
            lines.append("")
    else:
        # v1 format fallback
        lines.append("### Score Breakdown")
        lines.append("")
        lines.append("| Article | Structural | Depth | Currency | Sourcing | Linking | Consistency | **Composite** |")
        lines.append("|---------|-----------|-------|----------|----------|---------|-------------|--------------|")
        for key in sorted(latest["articles"].keys()):
            a = latest["articles"][key]
            s = a["scores"]
            lines.append(f"| {key} | {s['structural']} | {s['depth']} | {s['currency']} | "
                         f"{s['sourcing']} | {s['linking']} | {s['consistency']} | **{s['composite']}** |")
        lines.append("")

    # ── Trend Over Time ──
    runs = []
    for f in files:
        with open(f) as fp:
            runs.append(json.load(fp))

    if len(runs) >= 2:
        lines.append("## Score Trend")
        lines.append("")
        overall_scores = [r["summary"]["overall_composite"] for r in runs]
        sparkline = _ascii_sparkline(overall_scores)
        lines.append(f"**Overall composite:** {overall_scores[0]:.1f} -> {overall_scores[-1]:.1f}  `{sparkline}`")
        lines.append("")

        # Load experiment logs for session notes
        experiment_notes = _load_experiment_notes()

        # SVG line chart
        lines.append("### Score Over Time")
        lines.append("")
        lines.append(_generate_trend_svg(runs, experiment_notes))
        lines.append("")

        # History table
        lines.append("### Run History")
        lines.append("")
        lines.append("| Timestamp | Ver | Quality | Topic Cov | Wishlist Cov | Combined | Articles | Notes |")
        lines.append("|-----------|-----|---------|-----------|-------------|----------|----------|-------|")
        prev_overall = None
        for r in runs:
            ts = r["timestamp"][:16].replace("T", " ")
            ver = r.get("version", 1)
            overall = r["summary"]["overall_composite"]
            topic_cov = r.get("topic_coverage", {})
            cov_score = topic_cov.get("coverage_score", "—") if topic_cov else "—"
            cov_str = f"{cov_score}" if isinstance(cov_score, (int, float)) else cov_score
            # Wishlist coverage
            wl_cov = r.get("wishlist_coverage", {})
            if wl_cov and wl_cov.get("total", 0) > 0:
                wl_score = wl_cov.get("coverage_score", "—")
                wl_str = f"{wl_score}%" if isinstance(wl_score, (int, float)) else str(wl_score)
            else:
                wl_str = "—"
                wl_score = None
            # Combined coverage
            tc_val = topic_cov.get("coverage_score") if topic_cov else None
            if isinstance(tc_val, (int, float)) and isinstance(wl_score, (int, float)):
                combined_val = (tc_val + wl_score) / 2
                combined_str = f"{combined_val:.0f}%"
            else:
                combined_str = "—"
            count = r["summary"]["article_count"]
            # Find matching experiment note (score changed = post-edit run)
            note = ""
            if prev_overall is not None and overall != prev_overall:
                note = experiment_notes.get(overall, "")
            prev_overall = overall
            lines.append(f"| {ts} | v{ver} | **{overall}** | {cov_str} | {wl_str} | {combined_str} | {count} | {note} |")
        lines.append("")

    # ── Open Issues ──
    lines.append("## Open Issues")
    lines.append("")
    issue_items = []
    for key in sorted(latest["articles"].keys(),
                      key=lambda k: latest["articles"][k]["scores"]["composite"]):
        a = latest["articles"][key]
        issues = a["issues"]
        if issues.get("missing_sections"):
            issue_items.append(f"- **{key}**: Missing sections -- {', '.join(issues['missing_sections'])}")
        if issues.get("missing_category_links"):
            issue_items.append(f"- **{key}**: Missing links to categories -- {', '.join(issues['missing_category_links'])}")
        for ci in issues.get("consistency_issues", [])[:2]:
            issue_items.append(f"- **{key}**: {ci}")

    if issue_items:
        lines.extend(issue_items[:15])
    else:
        lines.append("*No issues found -- all articles pass all checks.*")
    lines.append("")

    lines.append("---")
    lines.append("")
    lines.append("*This page is auto-generated by `benchmark_wiki.py`. Do not edit manually.*")

    TREND_MD.write_text("\n".join(lines))
    return TREND_MD


