#!/usr/bin/env python3
"""
Wikipedia for AI Learning -- Benchmark Script (v2)

Evaluates ALL wiki articles on quality and completeness, with tier-aware
scoring that adapts expectations to article size and category.

Tiers:
  - Flagship: Large frontier-topics articles (>500 lines) — full quality bar
  - Standard: Medium articles (100-500 lines) — moderate expectations
  - Reference: Short articles (<100 lines) — navigation hubs, linking matters most

Produces per-article scores grouped by category, coverage metrics, freshness
tracking, and a summary scorecard. Tracks scores over time in data/benchmarks/.

Usage:
    python3 benchmark_wiki.py              # Full benchmark
    python3 benchmark_wiki.py --diff       # Compare latest two benchmarks
    python3 benchmark_wiki.py --history    # Score trend over time
"""

