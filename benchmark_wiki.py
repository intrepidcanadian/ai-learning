import math
import os
import re
import sys
from datetime import datetime
from pathlib import Path
from collections import defaultdict

# ── Config ──────────────────────────────────────────────────────────────────

ROOT = Path(__file__).parent
BENCHMARK_DIR = ROOT / "data" / "benchmarks"

CATEGORIES = [
    "core-concepts",
    "tools-platforms",
    "methodologies",
    "frontier-topics",
    "research-sources",
]

# Files excluded from benchmarking
EXCLUDED_FILES = {
    "benchmark-trend.md",   # auto-generated by this script
    "latest-papers.md",     # auto-updated by cron
}

# Year threshold for "recent" references
RECENT_YEAR = 2025

# ── Tiers ───────────────────────────────────────────────────────────────────

FLAGSHIP = "flagship"
STANDARD = "standard"
REFERENCE = "reference"


def classify_article(path, line_count):
    """Determine tier based on category and size."""
    cat = path.parent.name
    if cat == "frontier-topics" and line_count >= 500:
        return FLAGSHIP
    elif line_count < 100 and cat in ("tools-platforms", "research-sources"):
        return REFERENCE
    else:
        return STANDARD


# ── Tier-specific parameters ────────────────────────────────────────────────

TIER_WEIGHTS = {
    FLAGSHIP:  {"structural": 0.15, "depth": 0.20, "currency": 0.15,
                "sourcing": 0.20, "linking": 0.10, "consistency": 0.15, "freshness": 0.05},
    STANDARD:  {"structural": 0.15, "depth": 0.15, "currency": 0.15,
                "sourcing": 0.15, "linking": 0.20, "consistency": 0.15, "freshness": 0.05},
    REFERENCE: {"structural": 0.10, "depth": 0.10, "currency": 0.10,
                "sourcing": 0.10, "linking": 0.30, "consistency": 0.25, "freshness": 0.05},
}

TIER_DEPTH_PARAMS = {
    FLAGSHIP:  {"content_div": 10, "h3_mult": 2, "app_mult": 3},
    STANDARD:  {"content_div": 5,  "h3_mult": 4, "app_mult": 5},
    REFERENCE: {"content_div": 3,  "h3_mult": 8, "app_mult": 10},
}

# Expected sections per tier
SECTION_ALIASES = {
    "overview": ["overview"],
    "background": ["background", "theoretical foundations", "key systems",
                   "key systems and results", "llm-powered shopping agents",
                   "the unified learning loop", "how it works", "architecture",
                   "core features", "features", "capabilities", "usage",
                   "key concepts", "approach", "framework"],
    "technical details": ["technical details", "how it works", "verification",
                          "the metacognition framework", "quality of simulation",
                          "predictive models for commerce", "benchmarks and evaluation",
                          "cross-cutting themes", "api", "endpoints", "configuration",
                          "implementation", "methods", "methodology"],
    "current state": ["current state", "latest developments", "industry trends",
                      "the iclr 2026 workshop", "iclr 2026", "implications",
                      "recent work", "status", "updates"],
    "limitations": ["limitations", "challenges", "open questions", "dark patterns",
                    "risks", "concerns", "trade-offs", "caveats"],
    "see also": ["see also", "connections to other topics", "related",
                 "further reading", "external links"],
    "references": ["references", "sources", "bibliography", "citations"],
}

REQUIRED_SECTIONS = {
    FLAGSHIP:  list(SECTION_ALIASES.keys()),  # all 7
    STANDARD:  ["overview", "background", "see also", "references"],  # 4
    REFERENCE: ["overview", "see also", "references"],  # 3
}


# ── Article Discovery ───────────────────────────────────────────────────────

def discover_articles():
    """Walk category directories and return {key: Path} for all .md articles."""
    articles = {}
    for cat in CATEGORIES:
        cat_dir = ROOT / cat
        if not cat_dir.is_dir():
            continue
        for md_file in sorted(cat_dir.glob("*.md")):
            if md_file.name in EXCLUDED_FILES:
                continue
            articles[md_file.stem] = md_file
    return articles


# ── Extraction Helpers ──────────────────────────────────────────────────────

def extract_sections(text):
    """Extract all H2 and H3 section headers from markdown."""
    headers = []
    for line in text.splitlines():
        m = re.match(r'^(#{2,3})\s+(.+)', line)
        if m:
            level = len(m.group(1))
            title = m.group(2).strip()
            headers.append({"level": level, "title": title})
    return headers


def extract_references(text):
    """Extract footnote references [^N] and their definitions."""
    usages = set(re.findall(r'\[\^(\d+)\]', text))
    definitions = set(re.findall(r'^\[\^(\d+)\]:', text, re.MULTILINE))
    return usages, definitions


def extract_internal_links(text):
    """Extract markdown links to other .md files."""
    links = re.findall(r'\[([^\]]+)\]\(([^)]+\.md[^)]*)\)', text)
    return links


def extract_years(text):
    """Extract all 4-digit years from references/citations."""
    years = [int(y) for y in re.findall(r'\b(20[0-9]{2})\b', text)]
    return years


# ── Scoring Functions ───────────────────────────────────────────────────────

def score_structural_completeness(text, headers, tier):
    """Score 0-100: does the article have the expected template sections?"""
    header_titles_lower = [h["title"].lower() for h in headers]
    required = REQUIRED_SECTIONS[tier]

    found = 0
    missing = []
    for section in required:
        aliases = SECTION_ALIASES.get(section, [section])
        if any(alias in title for alias in aliases for title in header_titles_lower):
            found += 1
        else:
            missing.append(section)

    score = int((found / len(required)) * 100) if required else 100
    return score, missing


def score_depth(text, headers, tier):
    """Score 0-100: how substantive is the content?"""
    lines = text.splitlines()
    params = TIER_DEPTH_PARAMS[tier]

    # Count non-empty, non-header content lines
    content_lines = [l for l in lines if l.strip() and not l.startswith('#')]
    content_count = len(content_lines)

    # Count H3 subsections
    h3_count = sum(1 for h in headers if h["level"] == 3)

    # Count code blocks, blockquotes, tables
    code_blocks = len(re.findall(r'```', text)) // 2
    blockquotes = sum(1 for l in lines if l.strip().startswith('>'))
    tables = sum(1 for l in lines if '|' in l and l.strip().startswith('|'))

    score = 0
    # Content volume (up to 30 points)
    score += min(30, content_count // params["content_div"])
    # Subsection detail (up to 25 points)
    score += min(25, h3_count * params["h3_mult"])
    # Rich content (up to 20 points)
    score += min(20, (code_blocks * 5 + blockquotes + tables))
    # Learning applications (up to 15 points)
    learning_apps = len(re.findall(r'\*\*Learning (?:application|connection)', text, re.IGNORECASE))
    score += min(15, learning_apps * params["app_mult"])
    # Diagrams (up to 10 points)
    diagrams = len(re.findall(r'```(?:mermaid|ascii|text|diagram)', text, re.IGNORECASE))
    ascii_art = len(re.findall(r'[┌┐└┘│─╔╗╚╝║═►→←↓↑▼▲]', text))
    score += min(10, diagrams * 5 + min(10, ascii_art // 5))

    return min(100, score)


def score_currency(text):
    """Score 0-100: how recent are the references?"""
    years = extract_years(text)
    if not years:
        return 0

    total = len(years)
    recent = sum(1 for y in years if y >= RECENT_YEAR)
    very_recent = sum(1 for y in years if y >= 2026)

    score = 0
    score += int((recent / total) * 50) if total else 0
    score += min(30, very_recent * 3)
    score += min(20, total // 2)

    return min(100, score)


def score_sourcing(text, tier):
    """Score 0-100: are claims backed by references?"""
    usages, definitions = extract_references(text)

    paragraphs = len(re.findall(r'\n\n[A-Z]', text))
    paragraphs = max(paragraphs, 1)

    # Tier-aware reference volume targets
    ref_vol_mult = {FLAGSHIP: 2, STANDARD: 4, REFERENCE: 8}

    score = 0
    # Reference volume (up to 40 points)
    score += min(40, len(definitions) * ref_vol_mult[tier])
    # Reference density (up to 30 points)
    density = len(usages) / paragraphs
    score += min(30, int(density * 30))
    # Orphaned footnotes penalty
    orphaned = usages - definitions
    score -= min(20, len(orphaned) * 5)
    # External links (up to 30 points)
    arxiv_links = len(re.findall(r'arxiv\.org', text))
    doi_links = len(re.findall(r'doi\.org', text))
    ext_mult = {FLAGSHIP: 2, STANDARD: 4, REFERENCE: 8}
    score += min(30, (arxiv_links + doi_links) * ext_mult[tier])

    return max(0, min(100, score))


def score_internal_linking(text, article_path, all_article_paths):
    """Score 0-100: internal link quality across the full wiki."""
    links = extract_internal_links(text)

    # Resolve all linked files
    linked_resolved = set()
    for _label, target in links:
        if target.startswith('http'):
            continue
        # Strip any anchor (#...) from target
        target_file = target.split('#')[0]
        if not target_file:
            continue
        # Resolve relative to article's directory
        resolved = (article_path.parent / target_file).resolve()
        if resolved.exists():
            linked_resolved.add(resolved)
        else:
            resolved = (ROOT / target_file).resolve()
            if resolved.exists():
                linked_resolved.add(resolved)

    own_category = article_path.parent.name
    all_resolved = {p.resolve() for p in all_article_paths}
    siblings = {p.resolve() for p in all_article_paths
                if p.parent.name == own_category and p.resolve() != article_path.resolve()}
    other_categories = set(p.parent.name for p in all_article_paths
                          if p.parent.name != own_category)

    score = 0

    # Sibling links (up to 40 points)
    if siblings:
        sibling_linked = len(linked_resolved & siblings)
        sibling_ratio = sibling_linked / len(siblings)
        score += int(min(40, sibling_ratio * 40))

    # Cross-category breadth (up to 30 points)
    categories_linked = set()
    for lf in linked_resolved:
        cat = lf.parent.name
        if cat != own_category and cat in other_categories:
            categories_linked.add(cat)
    if other_categories:
        breadth_ratio = len(categories_linked) / len(other_categories)
        score += int(min(30, breadth_ratio * 30))

    # Total internal link volume (up to 20 points)
    total_internal = len([l for l in links if not l[1].startswith('http')])
    score += min(20, total_internal * 2)

    missing_siblings = {p.stem for p in siblings} - {p.stem for p in (linked_resolved & siblings)}
    missing_categories = other_categories - categories_linked

    return min(100, score), missing_siblings, missing_categories


def score_consistency(text, headers, article_path=None):
    """Score 0-100: formatting consistency, duplicate headers, numbering issues."""
    score = 100
    issues = []

    # Duplicate H2 headers
    h2_titles = [h["title"].lower() for h in headers if h["level"] == 2]
    dupes = [t for t in set(h2_titles) if h2_titles.count(t) > 1]
    if dupes:
        score -= len(dupes) * 10
        issues.append(f"Duplicate H2 sections: {dupes}")

    # Duplicate H3 headers
    h3_titles = [h["title"].lower() for h in headers if h["level"] == 3]
    h3_dupes = [t for t in set(h3_titles) if h3_titles.count(t) > 1]
    if h3_dupes:
        score -= len(h3_dupes) * 5
        issues.append(f"Duplicate H3 sections: {h3_dupes}")

    # Connection numbering gaps (cross-cutting-connections)
    connections = re.findall(r'## Connection (\d+):', text)
    if connections:
        nums = [int(n) for n in connections]
        expected_seq = list(range(min(nums), max(nums) + 1))
        gaps = set(expected_seq) - set(nums)
        if gaps:
            score -= min(20, len(gaps) * 2)
            issues.append(f"Connection numbering gaps: {sorted(gaps)}")
        conn_dupes = [n for n in set(nums) if nums.count(n) > 1]
        if conn_dupes:
            score -= len(conn_dupes) * 5
            issues.append(f"Duplicate connection numbers: {conn_dupes}")

    # Broken internal links
    links = extract_internal_links(text)
    article_dir = article_path.parent if article_path else ROOT / "frontier-topics"
    for label, target in links:
        if target.startswith('http'):
            continue
        target_file = target.split('#')[0]
        if not target_file:
            continue
        # Resolve relative to the article's own directory first
        resolved = (article_dir / target_file).resolve()
        if not resolved.exists():
            resolved = ROOT / target_file
            if not resolved.exists():
                score -= 3
                issues.append(f"Broken link: [{label}]({target})")

    return max(0, score), issues


def score_freshness(path):
    """Score 0-100: how recently was this article modified?"""
    try:
        mtime = datetime.fromtimestamp(os.path.getmtime(path))
        age_days = (datetime.now() - mtime).total_seconds() / 86400
    except OSError:
        return 5, 999.0

    # Exponential decay: today=100, 3 days=~74, 7 days=~50, 14 days=~25, 30 days=~5
    raw = 100 * math.exp(-0.1 * age_days)
    return max(5, min(100, int(raw))), round(age_days, 1)


# ── Coverage & Freshness Summary ────────────────────────────────────────────

def compute_coverage(article_results):
    """Wiki-wide coverage analysis."""
    articles = list(article_results.values())
    by_category = defaultdict(list)
    for a in articles:
        cat = Path(a["file"]).parts[0]
        by_category[cat].append(a)

    total = len(articles)
    stubs = [a for a in articles if a["lines"] < 100]
    substantial = [a for a in articles if a["lines"] >= 200]

    return {
        "total_articles": total,
        "by_category": {cat: len(arts) for cat, arts in sorted(by_category.items())},
        "stubs": sorted([a["key"] for a in stubs]),
        "stub_count": len(stubs),
        "stub_pct": round(len(stubs) / total * 100, 1) if total else 0,
        "substantial_count": len(substantial),
        "substantial_pct": round(len(substantial) / total * 100, 1) if total else 0,
        "total_words": sum(a["words"] for a in articles),
        "total_lines": sum(a["lines"] for a in articles),
    }


def compute_freshness_summary(article_results):
    """Wiki-wide freshness analysis."""
    ages = [r["age_days"] for r in article_results.values()]
    if not ages:
        return {}
    return {
        "updated_today": sum(1 for a in ages if a < 1),
        "updated_this_week": sum(1 for a in ages if a < 7),
        "stale_articles": sorted([r["key"] for r in article_results.values() if r["age_days"] > 14]),
        "freshness_pct": round(sum(1 for a in ages if a < 7) / len(ages) * 100, 1),
        "median_age_days": round(sorted(ages)[len(ages) // 2], 1),
    }


# ── Main Benchmark ──────────────────────────────────────────────────────────

def benchmark_article(key, path, all_article_paths):
    """Run all scoring dimensions on a single article."""
    text = path.read_text()
    headers = extract_sections(text)
    line_count = len(text.splitlines())
    word_count = len(text.split())
    category = path.parent.name

    tier = classify_article(path, line_count)

    structural_score, missing_sections = score_structural_completeness(text, headers, tier)
    depth_score = score_depth(text, headers, tier)
    currency_score = score_currency(text)
    sourcing_score = score_sourcing(text, tier)
    linking_score, missing_siblings, missing_categories = score_internal_linking(
        text, path, all_article_paths)
    consistency_score, consistency_issues = score_consistency(text, headers, path)
    freshness_score, age_days = score_freshness(path)

    weights = TIER_WEIGHTS[tier]
    composite = (
        structural_score * weights["structural"]
        + depth_score * weights["depth"]
        + currency_score * weights["currency"]
        + sourcing_score * weights["sourcing"]
        + linking_score * weights["linking"]
        + consistency_score * weights["consistency"]
        + freshness_score * weights["freshness"]
    )

    # Collect outbound link stems for backlink bonus later
    links = extract_internal_links(text)
    outbound_stems = set()
    for _label, target in links:
        if target.startswith('http'):
            continue
        target_file = target.split('#')[0]
        if target_file:
            outbound_stems.add(Path(target_file).stem)

    return {
        "key": key,
        "file": str(path.relative_to(ROOT)),
        "category": category,
        "tier": tier,
        "lines": line_count,
        "words": word_count,
        "age_days": age_days,
        "scores": {
            "structural": structural_score,
            "depth": depth_score,
            "currency": currency_score,
            "sourcing": sourcing_score,
            "linking": linking_score,
            "consistency": consistency_score,
            "freshness": freshness_score,
            "composite": round(composite, 1),
        },
        "weights": weights,
        "outbound_stems": sorted(outbound_stems),
        "issues": {
            "missing_sections": missing_sections,
            "missing_sibling_links": sorted(missing_siblings),
            "missing_category_links": sorted(missing_categories),
            "consistency_issues": consistency_issues,
        },
        "h2_count": sum(1 for h in headers if h["level"] == 2),
        "h3_count": sum(1 for h in headers if h["level"] == 3),
        "footnote_count": len(extract_references(text)[1]),
        "internal_link_count": len([l for l in links if not l[1].startswith('http')]),
    }


def apply_backlink_bonus(results):
    """Second pass: articles receiving inbound links get a bonus on linking score."""
    inbound_counts = defaultdict(int)
    for _key, r in results.items():
        for stem in r.get("outbound_stems", []):
            inbound_counts[stem] += 1

    for key, r in results.items():
        bonus = min(10, inbound_counts.get(key, 0) * 2)
        if bonus > 0:
            old_linking = r["scores"]["linking"]
            r["scores"]["linking"] = min(100, old_linking + bonus)
            # Recompute composite
            weights = r["weights"]
            r["scores"]["composite"] = round(sum(
                r["scores"][dim] * weights[dim]
                for dim in weights
            ), 1)


def run_benchmark():
    """Benchmark all wiki articles and return results."""
    discovered = discover_articles()
    all_paths = list(discovered.values())

    results = {}
    for key, path in discovered.items():
        if not path.exists():
            continue
        results[key] = benchmark_article(key, path, all_paths)

    # Apply backlink bonus
    apply_backlink_bonus(results)

    # Compute wiki-level metrics
    coverage = compute_coverage(results)
    freshness = compute_freshness_summary(results)

    # Per-tier composites
    tier_scores = defaultdict(list)
    for r in results.values():
        tier_scores[r["tier"]].append(r["scores"]["composite"])
    tier_composites = {
        tier: round(sum(scores) / len(scores), 1)
        for tier, scores in tier_scores.items()
        if scores
    }

    if results:
        composites = [r["scores"]["composite"] for r in results.values()]
        summary = {
            "overall_composite": round(sum(composites) / len(composites), 1),
            "article_count": len(results),
            "total_lines": sum(r["lines"] for r in results.values()),
            "total_words": sum(r["words"] for r in results.values()),
            "lowest_scoring": min(results.values(), key=lambda r: r["scores"]["composite"])["key"],
            "highest_scoring": max(results.values(), key=lambda r: r["scores"]["composite"])["key"],
            "tier_composites": tier_composites,
        }
    else:
        summary = {}

    return {
        "timestamp": datetime.now().isoformat(),
        "version": 2,
        "summary": summary,
        "coverage": coverage,
        "freshness": freshness,
        "articles": results,
    }


def save_benchmark(results):
    """Save benchmark results to data/benchmarks/."""
    BENCHMARK_DIR.mkdir(parents=True, exist_ok=True)
    filename = f"{datetime.now().strftime('%Y-%m-%d_%H%M%S')}.json"
    filepath = BENCHMARK_DIR / filename
    with open(filepath, "w") as f:
        json.dump(results, f, indent=2, default=str)
    return filepath


